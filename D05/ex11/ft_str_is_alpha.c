int		ft_str_is_alpha(char *str)
{
	int		i;
	int		res;

	i = 0;
	res = 1;
	if (!str[i])
		return (1);
	while (str[i])
	{
		if (str[i] < 'A' || (str[i] > 'Z' && str[i] < 'a') || str[i] > 'z')
		{
			res = 0;
			break ;
		}
		i++;
	}
	return (res);
}
