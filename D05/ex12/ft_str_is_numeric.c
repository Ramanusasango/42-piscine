int		ft_str_is_numeric(char *str)
{
	int		i;
	int		res;

	i = 0;
	res = 1;
	if (!str[i])
		return (1);
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
		{
			res = 0;
			break ;
		}
		i++;
	}
	return (res);
}
